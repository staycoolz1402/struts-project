package com.ams.mufins.webservice.model;

public class SlidesMobile {
	
	private long slidesMobileId;
	private String name;
	private String image;
	private String link;
	private String packageName;
	private Character publish;
	private byte[] fileContent;
	/**
	 * @return the slidesMobileId
	 */
	public long getSlidesMobileId() {
		return slidesMobileId;
	}
	/**
	 * @param slidesMobileId the slidesMobileId to set
	 */
	public void setSlidesMobileId(long slidesMobileId) {
		this.slidesMobileId = slidesMobileId;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the image
	 */
	public String getImage() {
		return image;
	}
	/**
	 * @param image the image to set
	 */
	public void setImage(String image) {
		this.image = image;
	}
	/**
	 * @return the link
	 */
	public String getLink() {
		return link;
	}
	/**
	 * @param link the link to set
	 */
	public void setLink(String link) {
		this.link = link;
	}
	/**
	 * @return the packageName
	 */
	public String getPackageName() {
		return packageName;
	}
	/**
	 * @param packageName the packageName to set
	 */
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	/**
	 * @return the publish
	 */
	public Character getPublish() {
		return publish;
	}
	/**
	 * @param publish the publish to set
	 */
	public void setPublish(Character publish) {
		this.publish = publish;
	}
	/**
	 * @return the fileContent
	 */
	public byte[] getFileContent() {
		return fileContent;
	}
	/**
	 * @param fileContent the fileContent to set
	 */
	public void setFileContent(byte[] fileContent) {
		this.fileContent = fileContent;
	}

}
