package com.ams.mufins.webservice.model;

import java.util.Date;

public class DetailPersonalId {
	
	public Date applicationDate;
	public String status;
	public long vehicleCollRefId;
	public String vehicleCollateralRefName;
	public String leaseBack;

	
	public String getLeaseBack() {
		return leaseBack;
	}

	public void setLeaseBack(String leaseBack) {
		this.leaseBack = leaseBack;
	}

	public Date getApplicationDate() {
		return applicationDate;
	}

	public void setApplicationDate(Date applicationDate) {
		this.applicationDate = applicationDate;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public long getVehicleCollRefId() {
		return vehicleCollRefId;
	}

	public void setVehicleCollRefId(long vehicleCollRefId) {
		this.vehicleCollRefId = vehicleCollRefId;
	}

	public String getVehicleCollateralRefName() {
		return vehicleCollateralRefName;
	}

	public void setVehicleCollateralRefName(String vehicleCollateralRefName) {
		this.vehicleCollateralRefName = vehicleCollateralRefName;
	}
	
}
